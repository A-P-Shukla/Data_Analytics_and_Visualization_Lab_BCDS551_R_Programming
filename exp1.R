# Define the array 
my_array <- c(12, 45, 67, 89, 34, 56, 23, 78, 90, 5) 
# Calculate maximum 
max_value <- max(my_array) 
cat("Maximum:", max_value, "\n") 
# Calculate minimum 
min_value <- min(my_array) 
cat("Minimum:", min_value, "\n") 
# Calculate average 
avg_value <- mean(my_array) 
cat("Average:", avg_value, "\n") 
# Calculate sum 
sum_value <- sum(my_array) 
cat("Sum:", sum_value, "\n") 
# Calculate square root 
sqrt_array <- sqrt(my_array) 
cat("Square Roots:", sqrt_array, "\n") 
# Round each element in the array 
rounded_array <- round(my_array) 
cat("Rounded Array:", rounded_array, "\n")